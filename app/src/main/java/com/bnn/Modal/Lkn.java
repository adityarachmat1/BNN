package com.bnn.Modal;

public class Lkn {
    private String nomor_lkn;
    private String tgl_pemusnahan;
    private String nm_brgbukti;
    private String jumlah_dimusnahkan;
    private String id;

    public String getNomor_lkn() {
        return nomor_lkn;
    }

    public void setNomor_lkn(String nomor_lkn) {
        this.nomor_lkn = nomor_lkn;
    }

    public String getTgl_pemusnahan() {
        return tgl_pemusnahan;
    }

    public void setTgl_pemusnahan(String tgl_pemusnahan) {
        this.tgl_pemusnahan = tgl_pemusnahan;
    }

    public String getNm_brgbukti() {
        return nm_brgbukti;
    }

    public void setNm_brgbukti(String nm_brgbukti) {
        this.nm_brgbukti = nm_brgbukti;
    }

    public String getJumlah_dimusnahkan() {
        return jumlah_dimusnahkan;
    }

    public void setJumlah_dimusnahkan(String jumlah_dimusnahkan) {
        this.jumlah_dimusnahkan = jumlah_dimusnahkan;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
